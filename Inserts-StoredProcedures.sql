if (OBJECT_ID ('UPSERVICE', 'P')) is not null
    DROP PROCEDURE UPSERVICE;
GO
CREATE PROCEDURE UPSERVICE
 @ID_SERVICIO VARCHAR(10),
 @NOMBRE_BRIN VARCHAR(25),
 @DESCRIPCION1 NVARCHAR(300),
 @AREA_BUSQ VARCHAR(30),
 @NOMBRE_BUSQ VARCHAR(25),
 @DESCRIPCION2 NVARCHAR(300),
 @VALORPROME FLOAT
AS
BEGIN
	UPDATE SERVICIO SET NOMBRE_BRIN = @NOMBRE_BRIN, DESCRIPCION1 = @DESCRIPCION1, AREA_BUSQ = @AREA_BUSQ, NOMBRE_BUSQ = @NOMBRE_BUSQ, DESCRIPCION2  = @DESCRIPCION2, VALORPROME = @VALORPROME WHERE ID_SERVICIO = @ID_SERVICIO
END


