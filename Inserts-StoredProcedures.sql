                          SERVICIO.DESCRIPCION2, SERVICIO.VALORPROME
FROM             USUARIO INNER JOIN
                          SERVICIO ON USUARIO.USERACCOUNT = SERVICIO.USERACCOUNT
WHERE         (SERVICIO.ID_SERVICIO = @ID_SERVICIO)