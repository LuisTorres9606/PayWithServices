 CREATE PROCEDURE ADDUSER
 @USERACCOUNT NVARCHAR(15),
 @PASSWORD NVARCHAR(20),
 @PROFILEPICTURE IMAGE,
 @CEDULA VARCHAR(9),
 @NOMBRE NVARCHAR(20),
 @APELLIDOS NVARCHAR(25),
 @SEXO NVARCHAR(15),
 @EMAIL NVARCHAR(30),
 @TELEFONO VARCHAR(12),
 @AREALAB VARCHAR(30)
 AS
 BEGIN
 INSERT INTO USUARIO VALUES(@USERACCOUNT,@PASSWORD,@PROFILEPICTURE,@CEDULA,@NOMBRE,@APELLIDOS,@SEXO,@EMAIL,@TELEFONO,@AREALAB)
 END


